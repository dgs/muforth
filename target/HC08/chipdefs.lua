function show_sfrs(s)
   pat = "(0x%x+) (%w+)" .. (" ([%w<-]+)"):rep(8)
   for addr, reg, b7, b6, b5, b4, b3, b2, b1, b0 in s:gmatch(pat) do
      print (string.format("%04x r| %-8s | "..("%-7s "):rep(8),
                           addr, reg, b7, b6, b5, b4, b3, b2, b1, b0))
   end
end

